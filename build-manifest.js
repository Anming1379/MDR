// build-manifest.js
const fs = require('fs');
const path = require('path');

const textDir = path.join(__dirname, 'Text'); // Text/ 文件夹
const outFile = path.join(__dirname, 'manifest.js'); // 输出到 manifest.js

function walk(dir, base = '') {
  const res = {};
  const list = fs.existsSync(dir) ? fs.readdirSync(dir) : [];
  for (const name of list) {
    const full = path.join(dir, name);
    const rel = base ? `${base}/${name}` : name;
    const stat = fs.statSync(full);
    if (stat.isDirectory()) {
      Object.assign(res, walk(full, rel));
    } else if (stat.isFile() && name.toLowerCase().endsWith('.md')) {
      const txt = fs.readFileSync(full, 'utf8');
      // 转义反斜杠和反引号，保留换行 \n
      res[rel.replace(/\\/g, '/')] = txt;
    }
  }
  return res;
}

const manifest = walk(textDir);

// 输出为一个 JS 模块式全局变量 filesManifest
const content = `/* manifest generated by build-manifest.js */
window.filesManifest = ${JSON.stringify(manifest, null, 2)};
`;

fs.writeFileSync(outFile, content, 'utf8');
console.log('✅ manifest.js generated with', Object.keys(manifest).length, 'files');